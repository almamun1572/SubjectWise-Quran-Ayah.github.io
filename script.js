function showVerse(category, buttonElement) {
  const verseDisplay = document.getElementById("verseDisplay");
  const selectedVerses = verses[category];

  // Clear the container first
  verseDisplay.innerHTML = "";

  // Display the button's text (e.g., "সবর")
  const categoryText = buttonElement.textContent || buttonElement.innerText;
  verseDisplay.innerHTML = `<h2 class="verse-header ${category}">${categoryText}</h2>`;

  if (!selectedVerses || selectedVerses.length === 0) {
    verseDisplay.innerHTML += `<p>কোনো আয়াত পাওয়া যায়নি।</p>`;
    return;
  }

  // Display the selected category's verses
  verseDisplay.innerHTML += selectedVerses
    .map(
      (verse) => `
      <div class="verse-container">
        <div class="verse-info-container">
          <div class="info-block"><strong>পারা নম্বর:</strong> ${verse.paraNumber},</div> 
          <div class="info-block"><strong>সূরা নম্বর:</strong> ${verse.surahNumber},</div> 
          <div class="info-block"><strong>সূরা নাম:</strong> ${verse.surahName},</div> 
          <div class="info-block"><strong>আয়াত নম্বর:</strong> ${verse.ayatNumber}</div>
        </div>
        <div class="verse-arabic">${verse.arabic}</div>
        <div class="verse-translation">${verse.translation}</div>
      </div>
    `
    )
    .join("");

  // Fade-in animation
  verseDisplay.style.opacity = 0;
  verseDisplay.scrollIntoView({ behavior: "smooth" });
  setTimeout(() => {
    verseDisplay.style.opacity = 1;
    verseDisplay.style.transition = "opacity 1s ease-in-out";
  }, 100);
}

